package figures;

/**
 * Enum representing the different types of chess pieces available in a standard game.
 * <p>
 * These values are used to identify the type of a {@link Figure}.
 * </p>
 * 
 * @author Jose Luis Hernandez
 * @version 5.1
 */

public enum PieceName {
     /** Represents the King piece. */
     KING,

     /** Represents the Queen piece. */
     QUEEN,
 
     /** Represents the Bishop piece. */
     BISHOP,
 
     /** Represents the Knight piece. */
     KNIGHT,
 
     /** Represents the Rook piece. */
     ROOK,
 
     /** Represents the Pawn piece. */
     PAWN
 }